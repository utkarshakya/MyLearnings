package j05_strings;

public class EscapeSequence {
    public static void main(String[] args) {
        // Escape sequence characters consist of more than one character but represents one character when used within a string
        // Some of them are:
        // \n --> newline, \t --> tab, \' --> singlequote, \\ --> backslash, etc
        System.out.println("Hello I am backslash \\");
        System.out.println("Hello I am tab \t Hi...");
        System.out.println("Hello I am Double quote \"");
    }
}
